# Extract Color From Image
import colorgram

rgb = []
colors = colorgram.extract('dot.jpg', 100)
for color in colors:
    red = color.rgb.r
    green = color.rgb.g
    blue = color.rgb.b
    new_color = (red, green, blue)
    rgb.append(new_color)

